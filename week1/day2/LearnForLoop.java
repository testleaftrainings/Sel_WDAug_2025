package week1.day2;

public class LearnForLoop {

	public static void main(String[] args) {
		/*
		 * System.out.println("1"); System.out.println("2"); System.out.println("3");
		 */
		//
		//shortcut--->type for and do ctrl+space
		//syntax:for(initialization; condn;iteration)
		for (int i = 1; i <=10; ++i) {
			System.out.println(i);
			
		}
		System.out.println("--------------------");
		for (int i = 10; i >=1 ; i--) {
			System.out.println(i);
			
		}
	}

}
